public class Variables34 {
    public static void main(String[] args) {
        // Valores máximos y mínimos para int
        System.out.println("Valores para int:");
        System.out.println("Máximo valor: " + Integer.MAX_VALUE);
        System.out.println("Mínimo valor: " + Integer.MIN_VALUE);

        // Valores máximos y mínimos para double
        System.out.println("\nValores para double:");
        System.out.println("Máximo valor: " + Double.MAX_VALUE);
        System.out.println("Mínimo valor: " + Double.MIN_VALUE);

        // Valores máximos y mínimos para char
        System.out.println("\nValores para char:");
        System.out.println("Máximo valor (en Unicode): " + (int)Character.MAX_VALUE);
        System.out.println("Mínimo valor (en Unicode): " + (int)Character.MIN_VALUE);
    }
}
